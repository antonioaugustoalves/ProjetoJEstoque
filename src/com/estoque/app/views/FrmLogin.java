    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String login = txtLogin.getText();
        String senha = new String(txtSenha.getPassword()).trim();
        Usuario usuario = new Usuario();
        usuario.setLogin(login);
        usuario.setSenha(senha);

        LoginController controller = new LoginController(usuario);
        Usuario logado = controller.efetuarLogin();
        if (logado != null) {
            JOptionPane.showMessageDialog(null,
                    "Bem vindo, " + logado.getNome());
            FrmPrincipal form = new FrmPrincipal();
            form.setExtendedState(JFrame.MAXIMIZED_BOTH);
            form.setLocationRelativeTo(form);
            form.setVisible(true);
            this.dispose();
        } else {
            
            if (contador == 0) {
                JOptionPane.showMessageDialog(null,
                        "Você execedeu o numero de tentativas."
                        + "\nFale com o fornecedor do sistema.");
                System.exit(0);
            } else {
                JOptionPane.showMessageDialog(null,
                        "Dados inválidos!"
                        + "\nVerifique seu login e senha!"
                        + "\nTentativas restantes: " + contador);

            }
          contador--;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

